The asset investment market functions very similarly to the real life stock exchange. A good idea is to buy low and sell high. This is 
a very noob friendly stock exchange. Be careful, you may lose your investment!<hr />
<h3>Risk Level</h3>
Risk level sets when an asset will receive a ticker update. The higher the Risk Level, the more often it updates.<br />
Level 1: Once a Day<br />
Level 2: Once Every 6 Hours<br />
Level 3: Once Every Hour<br />
Level 4: Once Every 5 Minutes<br />
Level 5: Once a Minute<hr />
<h3>History</h3>
You can view an asset's history to see how it's performing. When the value of the tick is bold and green, it means it's reached 
the maximum it can go, and will drop. If its red and bold, it's value has bottomed out, and will only go up. Consider it a safe net on us. ;)<hr />
<h3>Fees</h3>
Note that there is only a 0.02% selling fee when you sell off your assets.
